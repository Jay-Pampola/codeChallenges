let myAge = 3; //this is my age
let earlyYears = 2; // early years
earlyYears *= 10.5;
let laterYears = myAge - 2; //later years
laterYears *= 4; //dog years
let myAgeInDogYears = earlyYears + laterYears; //my age in dog years
let myName = 'Jay'.toLowerCase(); //my name
console.log(`My name is ${myName}. I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.`);